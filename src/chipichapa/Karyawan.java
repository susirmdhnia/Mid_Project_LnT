package chipichapa;

public class Karyawan {
	String kodeKaryawan;
	String namaKaryawan;
	String gender;
	String jabatan;
	double gaji;
	
	
	public Karyawan(String kodeKaryawan, String namaKaryawan, String gender, String jabatan, double gaji) {
		super();
		this.kodeKaryawan = kodeKaryawan;
		this.namaKaryawan = namaKaryawan;
		this.gender = gender;
		this.jabatan = jabatan;
		this.gaji = gaji;
	}

}
